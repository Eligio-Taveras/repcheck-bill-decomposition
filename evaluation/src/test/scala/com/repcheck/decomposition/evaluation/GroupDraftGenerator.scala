package com.repcheck.decomposition.evaluation

import java.nio.charset.StandardCharsets
import java.nio.file.{Files, Paths}

import cats.effect.unsafe.implicits.global

import io.circe.Printer
import io.circe.syntax._

import com.repcheck.decomposition.conformance.{ConformanceContract, Corpus}
import com.repcheck.decomposition.evaluation.cluster.AgglomerativeClusterer
import com.repcheck.decomposition.evaluation.wiring.EmbeddingHarness
import com.repcheck.decomposition.text.{DefaultSectionParser, TextFormat}
import com.repcheck.utils.tags.E2ETest

/**
 * Regenerates DRAFT concept groupings (label kind (b)) for the pilot bills by embedding each section and clustering
 * those vectors (DP0-4a). Boundaries are re-derived from the parser (identical to DP0-1), groups come from the
 * [[AgglomerativeClusterer]], and `labelStatus` moves to `draft-groups`. Concept labels + taxonomy node ids stay empty
 * â€” those are DP0-4b (LLM) + human review. E2ETest (live Ollama) + forked update gate:
 *
 * sbt -Dupdate.gold=true [-Dcluster.threshold=0.3] "evaluation/testOnly *GroupDraftGenerator -- -n
 * com.repcheck.tags.E2ETest"
 */
class GroupDraftGenerator extends ConformanceContract {

  private val canonical  = Printer.spaces2.copy(sortKeys = true)
  private val parser     = new DefaultSectionParser
  private def updateMode = sys.props.get("update.gold").contains("true")
  // 0.45 cosine-distance (cosine ~0.55) â€” empirically ~40% grouping on the pilot (18â†’7, 26â†’9); a sane review
  // starting point, NOT the final cut. The production cut/threshold is tuned against the gold in DP0-5.
  private def threshold: Double = sys.props.get("cluster.threshold").map(_.toDouble).getOrElse(0.45)

  GoldPilot.versionIds.foreach { vid =>
    s"draft groups for $vid" should "be regenerated by clustering section embeddings" taggedAs E2ETest in {
      assume(updateMode, "run forked with -Dupdate.gold=true to regenerate")
      val bill     = Corpus.bills.find(_.versionId == vid).getOrElse(fail(s"$vid not in corpus"))
      val parsed   = parser.parse(bill.content, TextFormat.fromFormatType(bill.format))
      val contents = parsed.sections.map(_.content)

      EmbeddingHarness.resource().use(_.embedBatch(contents)).attempt.unsafeRunSync() match {
        case Left(err) =>
          cancel(s"Ollama embedding unreachable (${err.getMessage})")
        case Right(embeddings) =>
          val vectors = embeddings.map(a => a.iterator.map(_.toDouble).toVector).toIndexedSeq
          val labels  = AgglomerativeClusterer.cluster(vectors, threshold)
          val groups = labels.zipWithIndex
            .groupMap(_._1)(_._2)
            .toList
            .sortBy(_._1)
            .map {
              case (cid, idxs) => GoldGroup(s"g$cid", idxs.sorted.toList, conceptLabel = "", taxonomyNodeIds = Nil)
            }
          val gold = GoldBill(
            versionId = vid,
            billType = bill.billType,
            format = bill.format,
            labelStatus = "draft-groups",
            parserUsed = parsed.parserUsed.toString,
            sections = parsed.sections.map(s =>
              GoldSection(s.sectionIndex, s.sectionIdentifier, s.heading, s.kind.toString, s.content.length)
            ),
            groups = groups,
          )
          val dir = Paths.get("src", "main", "resources", "gold")
          val _   = Files.createDirectories(dir)
          val _ = Files.write(dir.resolve(s"$vid.json"), canonical.print(gold.asJson).getBytes(StandardCharsets.UTF_8))
          info(s"$vid: ${parsed.sections.size} sections â†’ ${groups.size} draft groups (threshold $threshold)")
          succeed
      }
    }
  }

}
